const Input = ({
  label,
  id,
  type = "text", // Default of "text"
  required,
  register,
  errors,
  disabled,
}: InputProps) => {
  return (
    <div>
      <label
        htmlFor={id}
        className="
          block 
          text-sm 
          font-medium 
          leading-6 
          text-gray-900
        "
      >
        {label}
      </label>
      <div className="mt-2">
        <input />
      </div>
    </div>
  );
};

export default Input;
